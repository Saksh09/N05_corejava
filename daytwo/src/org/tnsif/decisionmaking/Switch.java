package org.tnsif.decisionmaking;

public class Switch {

	public static void main(String[] args) {
		int day=2;
		switch(day) {
		case 1:
			System.out.println("Day one");
			break;
		
		case 2:
			System.out.println("Day two");
			break;
			
			
		case 3:
			System.out.println("Day three");
			break;
		default:
		
			System.out.println("Invalid Input");
		}

	}

}
